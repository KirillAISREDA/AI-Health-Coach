# ─── Парсер числительных ─────────────────────────────────────────────────────

_NUMBERS = {
    "один": 1, "одна": 1, "одно": 1, "одну": 1,
    "два": 2, "две": 2, "три": 3, "четыре": 4, "пять": 5,
    "шесть": 6, "семь": 7, "восемь": 8, "девять": 9, "десять": 10,
    "пол": 0.5, "половина": 0.5, "половину": 0.5, "полтора": 1.5,
}

# Единицы веса
_WEIGHT_PATTERN = re.compile(
    r'(\d+[.,]?\d*)\s*(?:г|гр|грамм|грамов|gram[s]?)\b', re.IGNORECASE
)


class FoodDatabase:
    """Локальная база продуктов с парсером текстового ввода."""

    def __init__(self):
        # Строим индекс: все возможные названия → ключ в _FOODS
        self._index: dict[str, str] = {}
        for key, (_, _, _, _, _, aliases) in _FOODS.items():
            self._index[key.lower()] = key
            for alias in aliases:
                self._index[alias.lower()] = key

    def lookup(self, name: str) -> Optional[dict]:
        """Поиск продукта по названию. Возвращает КБЖУ на 100г + дефолтный вес."""
        name_lower = name.lower().strip()

        # Точное совпадение
        if name_lower in self._index:
            return self._get_food(self._index[name_lower])

        # Подстрока
        for key_lower, food_key in self._index.items():
            if key_lower in name_lower or name_lower in key_lower:
                return self._get_food(food_key)

        return None

    def _get_food(self, key: str) -> dict:
        cal, p, f, c, default_w, _ = _FOODS[key]
        return {
            "name": key,
            "cal_100": cal,
            "protein_100": p,
            "fat_100": f,
            "carbs_100": c,
            "default_weight": default_w,
        }

    def estimate_from_text(self, text: str) -> Optional[dict]:
        """
        Парсит текстовое описание еды и возвращает структурированный dict
        в том же формате, что и AI-ответ.

        Понимает:
        - "два яйца и тост" → яйцо ×2 (60г) + тост (30г)
        - "гречка 200г с курицей 150г"
        - "куриная котлета 100гр, морская капуста 80гр"

        Возвращает None если ничего не распознано.
        """
        # Разбиваем на части по разделителям
        # Важно: " и " — только как отдельное слово, не буква в слове
        parts = re.split(r'[,;]\s*|\s+и\s+|\s*\+\s*', text.lower())
        parts = [p.strip() for p in parts if p.strip()]

        items = []
        for part in parts:
            parsed = self._parse_part(part)
            if parsed:
                items.append(parsed)

        # Если ничего не нашли — пробуем весь текст как одно название
        if not items:
            parsed = self._parse_part(text.lower())
            if parsed:
                items.append(parsed)

        if not items:
            return None

        # Собираем результат
        result_items = []
        total = {"weight_g": 0, "calories": 0, "protein": 0, "fat": 0, "carbs": 0}

        for item in items:
            name = item["name"]
            weight = item["weight_g"]
            ratio = weight / 100.0

            cal = round(item["cal_100"] * ratio)
            p   = round(item["protein_100"] * ratio, 1)
            f   = round(item["fat_100"] * ratio, 1)
            c   = round(item["carbs_100"] * ratio, 1)

            result_items.append({
                "name": name.capitalize(),
                "weight_g": weight,
                "calories": cal,
                "protein": p,
                "fat": f,
                "carbs": c,
            })

            total["weight_g"] += weight
            total["calories"] += cal
            total["protein"]  += p
            total["fat"]      += f
            total["carbs"]    += c

        # Округляем total
        total["calories"] = round(total["calories"])
        total["protein"]  = round(total["protein"], 1)
        total["fat"]      = round(total["fat"], 1)
        total["carbs"]    = round(total["carbs"], 1)

        return {
            "items": result_items,
            "total": total,
            "comment": "Оценка по базе продуктов (без AI).",
            "confidence": "low",
        }

    def _parse_part(self, part: str) -> Optional[dict]:
        """
        Парсит одну часть: "два яйца", "гречка 200г", "куриная котлета 100гр"
        Возвращает dict с name, weight_g, cal_100, protein_100, fat_100, carbs_100.
        """
        part = part.strip()
        if not part:
            return None

        # Ищем явный вес: "200г", "150 грамм"
        explicit_weight = None
        weight_match = _WEIGHT_PATTERN.search(part)
        if weight_match:
            explicit_weight = float(weight_match.group(1).replace(",", "."))
            part = _WEIGHT_PATTERN.sub("", part).strip()

        # Ищем числительное: "два", "3"
        quantity = 1.0
        # Цифровое
        num_match = re.match(r'^(\d+[.,]?\d*)\s+', part)
        if num_match:
            quantity = float(num_match.group(1).replace(",", "."))
            part = part[num_match.end():].strip()
        else:
            # Текстовое числительное
            for word, num in _NUMBERS.items():
                pattern = rf'\b{re.escape(word)}\b'
                if re.search(pattern, part):
                    quantity = num
                    part = re.sub(pattern, '', part).strip()
                    break

        # Убираем «шт», «штук» и подобное
        part = re.sub(r'\b(шт|штук[иа]?|порци[яюей])\b', '', part).strip()
        part = re.sub(r'\s+', ' ', part).strip()

        if not part:
            return None

        # Ищем продукт в базе
        food = self.lookup(part)
        if not food:
            return None

        # Определяем итоговый вес
        if explicit_weight:
            weight_g = explicit_weight
        else:
            weight_g = food["default_weight"] * quantity

        return {
            "name": food["name"],
            "weight_g": weight_g,
            "cal_100": food["cal_100"],
            "protein_100": food["protein_100"],
            "fat_100": food["fat_100"],
            "carbs_100": food["carbs_100"],
        }


