from aiogram import Router, F
from aiogram.types import Message
from aiogram.filters import Command

router = Router()

HELP_TEXT = """
🤖 <b>AI Health Coach — команды</b>

<b>Основное меню</b>
🥗 <b>Питание</b> — записать еду (фото или текст)
🏋️ <b>Тренировка</b> — план тренировки на сегодня
💧 <b>Вода</b> — быстро добавить воду
💊 <b>БАДы</b> — трекер добавок и проверка совместимости
😴 <b>Сон</b> — оценить качество сна
📊 <b>Статистика</b> — сводка за день + PDF-отчёт
⚙️ <b>Профиль</b> — обновить параметры

<b>Команды</b>
/start — начать заново / вернуться в меню
/report — PDF-отчёт за неделю
/reminders — настроить напоминания
/help — эта справка

<b>Свободный чат</b>
Просто напиши что угодно — спроси про рацион,
попроси совет по тренировке или напиши что съел.
Например:
• <i>«съел борщ и кусок хлеба»</i>
• <i>«болит плечо, что делать?»</i>
• <i>«составь план питания на завтра»</i>

⚠️ <i>Все рекомендации носят информационный характер.
Проконсультируйся с врачом перед изменением режима.</i>
""".strip()


@router.message(Command("help"))
@router.message(F.text == "/help")
async def cmd_help(message: Message):
    await message.answer(HELP_TEXT, parse_mode="HTML")
